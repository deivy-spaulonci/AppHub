package com.br.collections;

public class CollectionExample {
    /**
     * A API responsável pelas coleções em Java é chamada de Java Collections Framework (JCF). Essa estrutura fornece
     * classes e interfaces para trabalhar com grupos de objetos de maneira eficiente.
     * Aqui estão os principais componentes da Java Collections Framework:
     *
     *
     * Principais Interfaces
     * Collection: A interface raiz para todas as coleções.
     * List: Uma coleção ordenada que pode conter elementos duplicados. Implementações incluem:
     * ArrayList
     * LinkedList
     * Vector
     *
     *
     * Set: Uma coleção que não permite elementos duplicados. Implementações incluem:
     * HashSet
     * TreeSet
     * LinkedHashSet
     *
     *
     *
     * Map: Uma coleção de pares chave-valor. As chaves são únicas. Implementações incluem:
     * HashMap
     * TreeMap
     * LinkedHashMap
     *
     *
     *
     * Queue: Uma coleção que representa uma fila, permitindo a inserção e remoção de elementos em uma ordem específica. Implementações incluem:
     * LinkedList
     * PriorityQueue
     * Principais Classes
     *
     *
     *
     * ArrayList: Implementação de uma lista que usa um array redimensionável. É eficiente para acesso a elementos,
     * mas pode ser lento para inserções e remoções em posições intermediárias.
     *
     * LinkedList: Implementação de uma lista que usa uma estrutura de lista encadeada. É eficiente para inserções e
     * remoções, mas o acesso por índice pode ser mais lento.
     *
     * HashSet: Implementação de um conjunto que usa uma tabela hash. É eficiente para operações de inserção, remoção e
     * busca.
     *
     * TreeSet: Implementação de um conjunto que mantém a ordem natural dos elementos ou uma ordem específica definida
     * por um comparador.
     *
     * HashMap: Implementação de um mapa que usa uma tabela hash. Permite a associação de chaves a valores, oferecendo
     * acesso rápido.
     *
     * TreeMap: Implementação de um mapa que mantém as chaves em ordem natural ou em uma ordem específica definida
     * por um comparador.
     */
}
